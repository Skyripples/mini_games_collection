const listeners = new Set();

function getStoredLanguage() {
  try {
    const storedLanguage = window.localStorage.getItem(STORAGE_KEY);
    if (storedLanguage && SUPPORTED_LANGUAGES.has(storedLanguage)) {
      return storedLanguage;
    }
  } catch (error) {
    return DEFAULT_LANGUAGE;
  }

  return DEFAULT_LANGUAGE;
}

let currentLanguage = getStoredLanguage();

function applyDocumentLanguage() {
  document.documentElement.lang = currentLanguage === "zh" ? "zh-Hant" : "en";
  document.title = t("app.title");
}

function formatTemplate(template, params) {
  return template.replace(/\{(\w+)\}/g, function (_, key) {
    return Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : `{${key}}`;
  });
}

export function t(key, params) {
  const languageMap = translations[currentLanguage] || translations[DEFAULT_LANGUAGE];
  const defaultMap = translations[DEFAULT_LANGUAGE];
  const template = languageMap[key] || defaultMap[key] || key;

  if (!params) {
    return template;
  }

  return formatTemplate(template, params);
}

export function getLanguage() {
  return currentLanguage;
}

export function setLanguage(language) {
  if (!SUPPORTED_LANGUAGES.has(language) || language === currentLanguage) {
    return;
  }

  currentLanguage = language;

  try {
    window.localStorage.setItem(STORAGE_KEY, language);
  } catch (error) {
    // Ignore storage failures and keep the in-memory language.
  }

  applyDocumentLanguage();
  translatePage();
  listeners.forEach(function (listener) {
    listener(currentLanguage);
  });
}

export function onLanguageChange(listener) {
  listeners.add(listener);
  return function unsubscribe() {
    listeners.delete(listener);
  };
}

export function translatePage(root = document) {
  root.querySelectorAll("[data-i18n]").forEach(function (element) {
    element.textContent = t(element.dataset.i18n);
  });

  root.querySelectorAll("[data-i18n-placeholder]").forEach(function (element) {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  applyDocumentLanguage();
}

export function initializeI18n() {
  applyDocumentLanguage();
  translatePage();
}
